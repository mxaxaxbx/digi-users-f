const canvas = ref(null);

function hexToRgb(hex) {
  hex = hex.replace("#", "").trim();

  if (hex.length === 3) {
    hex = hex.split("").map(c => c + c).join("");
  }

  const bigint = parseInt(hex, 16);
  return {
    r: (bigint >> 16) & 255,
    g: (bigint >> 8) & 255,
    b: bigint & 255
  };
}

function parseColor(color) {
  color = color.trim();

  if (color.startsWith("#")) return hexToRgb(color);

  if (color.startsWith("rgb")) {
    const nums = color.match(/\d+/g).map(Number);
    return { r: nums[0], g: nums[1], b: nums[2] };
  }

  // fallback
  return { r: 255, g: 255, b: 255 };
}

onMounted(() => {
  const c = canvas.value;
  const ctx = c.getContext("2d");

  let w, h, ny, nx, n;
  const d = 40;

  let mx = -100;
  let my = -100;

  let dots = [];
  let canvasData;
  let alphaindexes = [];

  const drag = 0.01;
  const pow = 0.04;

  let currentColor = parseColor(props.color);

  class Fly {
    constructor(x, y) {
      this.vx = 0;
      this.vy = 0;
      this.x = x;
      this.y = y;
      this.c = 0;
    }

    draw(canvasData) {
      const ix = Math.floor(this.x);
      const iy = Math.floor(this.y);

      let i = -6;
      let j = -6;

      const mdm = Math.hypot(mx - this.x, my - this.y);
      let a = Math.pow(2, -0.0002 * mdm * mdm);

      if (this.c && a < 0.2) {
        a = 0.2;
        this.c -= 1;
      } else if (!this.c && Math.random() > 0.9997) {
        this.c = 100;
      }

      if (a < 0.01) return;

      for (let t = 0; t < 169; t++) {
        const ox = ix + i;
        const oy = iy + j;

        const dx = ox - this.x;
        const dy = oy - this.y;

        const dm = dx * dx + dy * dy;
        const alpha =
          (1 - Math.sqrt(dm) / 7) * 100 +
          Math.max((2 - Math.sqrt(dm)) * 255, 0);

        if (alpha > 0 && ox < w && ox > 0) {
          const ind = (ox + oy * w) * 4 + 3;
          canvasData.data[ind] += alpha * a;
          alphaindexes.push(ind);
        }

        j++;
        if (j === 7) {
          j = -6;
          i++;
        }
      }
    }

    compute() {
      const dx = this.x - mx;
      const dy = this.y - my;
      const dm = Math.sqrt(dx * dx + dy * dy);

      const a = pow * Math.pow(2, -0.0002 * dm * dm);

      this.vx += (Math.random() * 0.1 - 0.05) - drag * this.vx - (a * dx) / dm;
      this.vy += (Math.random() * 0.1 - 0.05) - drag * this.vy - (a * dy) / dm;

      this.x = (this.x + this.vx) % w;
      this.y = (this.y + this.vy) % h;

      if (this.x < 0) this.x += w;
      if (this.y < 0) this.y += h;
    }
  }

  function fillBackgroundColor() {
    const { r, g, b } = currentColor;

    for (let s = 0; s < w * h; s++) {
      canvasData.data[s * 4 + 0] = r;
      canvasData.data[s * 4 + 1] = g;
      canvasData.data[s * 4 + 2] = b;
    }
  }

  function resize() {
    w = c.offsetWidth;
    h = c.offsetHeight;

    c.width = w;
    c.height = h;

    canvasData = ctx.createImageData(w, h);

    fillBackgroundColor();

    ny = Math.ceil(h / d) + 2;
    nx = Math.ceil(w / d) + 2;
    n = nx * ny;

    dots = [];

    let gx = -d * 0.5;
    let gy = -d * 0.5;

    for (let i = 0; i < ny; i++) {
      for (let j = 0; j < nx; j++) {
        dots.push(new Fly(gx, gy));
        gx += d;
      }
      gx = -d * 0.5;
      gy += d;
    }
  }

  function mousemove(e) {
    mx = e.clientX;
    my = e.clientY;
  }

  function animate() {
    for (let z = 0; z < n; z++) dots[z].compute();

    for (const u of alphaindexes) canvasData.data[u] = 0;
    alphaindexes = [];

    for (let z = 0; z < n; z++) dots[z].draw(canvasData);

    ctx.putImageData(canvasData, 0, 0);

    requestAnimationFrame(animate);
  }

  resize();
  animate();

  window.addEventListener("mousemove", mousemove);
  window.addEventListener("resize", resize);

  watch(
    () => props.color,
    newVal => {
      currentColor = parseColor(newVal);
      fillBackgroundColor();
      ctx.putImageData(canvasData, 0, 0);
    }
  );

  onBeforeUnmount(() => {
    window.removeEventListener("mousemove", mousemove);
    window.removeEventListener("resize", resize);
  });
});

const inner = ref<HTMLElement | null>(null)

let rafId = 0
let offset = 0
const speed = 0.2 // más bajo = más suave

function loop() {
  if (!inner.value) return

  offset -= speed

  const width = inner.value.scrollWidth / 2

  if (-offset >= width) {
    offset = 0
  }

  inner.value.style.transform = `translate3d(${offset}px, 0, 0)`
  rafId = requestAnimationFrame(loop)
}

onMounted(() => {
  rafId = requestAnimationFrame(loop)
})

onBeforeUnmount(() => {
  cancelAnimationFrame(rafId)
})

onMounted(() => {
